<h2>LUMINEERS</h2>
<p>Lumineers are a revolutionary new type of veneer using ultra-thin porcelain. 
<br /><br />
Most cases require no tooth reduction. Some cases need minimal preparation or cosmetic re-contouring in order to facilitate alignment. Hence most cases need no injections. 
<br /><br />
Lumineers are so versatile that they can be placed over existing crown and bridge work without needing to replace them. They are the perfect solution for stained, chipped, discoloured and slightly misaligned teeth. 
<br /><br />
Not all dentists offer Lumineers veneers.They need to be specially trained and certified. 
<br /><br />
In most cases Lumineers are completely reversible. This is largely due to the non reduction in tooth structure so that your natural teeth are still intact and strong.
<br />
<br />
For more information about our Lumineer options, please visit our full website. <a href="../confident_smile/services/lumineers.php">Please click here.</a></p>

