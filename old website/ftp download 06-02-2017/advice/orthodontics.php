    <h5>Features of Private Orthodontic treatment at Ferndale House Dental Practice:</h5>
    
    <ul class="list">
    <li>Available to Adults and Children</li>
    <li>Flexible appointments - no waiting lists</li>
    <li>Invisible braces (Invisalign) also available</li>
    <li>Options available for monthly instalments</li>
    <li>Copmlimentary Oral Health Education</li>
